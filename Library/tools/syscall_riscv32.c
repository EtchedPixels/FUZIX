/*
 *	Generate the syscall functions
 *
 *	RISCV passes the arguments in a1-a7 which covers more
 *	arguments than we need. Thus all we actually have to generate for
 *	anything (except maybe fork...) is a load of t1 with the call
 *	number and a trap. On return a1 holds the result and a0 errno
 */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include "syscall_name.h"

static char namebuf[128];

static void write_call(int n)
{
  FILE *fp;
  int saves = 0;
  snprintf(namebuf, 128, "fuzixriscv32/syscall_%s.S", syscall_name[n]);
  fp = fopen(namebuf, "w");
  if (fp == NULL) {
    perror(namebuf);
    exit(1);
  }
  /*
   *	We define _fork() as trashing all the registers except the return
   *	values and tp/gp. The userspace trick here allows us to avoid saving
   *	all the registers on syscall entry just in case we are doing a fork and
   *	have multiple udata blocks.
   */

  fprintf(fp, "\t.text\n\n"
	      "\t.globl %1$s\n"
	      "\t.type %1$s, @function\n"
	      "\t.align 2\n\n"
	      "%1$s:\n", syscall_name[n]);

  if (strcmp(syscall_name[n], "_fork") == 0) {
    fprintf(fp, "\taddi sp,sp,-48\n");
    fprintf(fp, "\tsw s0,0(sp)\n");
    fprintf(fp, "\tsw s1,4(sp)\n");
    fprintf(fp, "\tsw s2,8(sp)\n");
    fprintf(fp, "\tsw s3,12(sp)\n");
    fprintf(fp, "\tsw s4,16(sp)\n");
    fprintf(fp, "\tsw s5,20(sp)\n");
    fprintf(fp, "\tsw s6,24(sp)\n");
    fprintf(fp, "\tsw s7,28(sp)\n");
    fprintf(fp, "\tsw s8,32(sp)\n");
    fprintf(fp, "\tsw s9,36(sp)\n");
    fprintf(fp, "\tsw s10,40(sp)\n");
    fprintf(fp, "\tsw s11,44(sp)\n");
    saves = 1;
  }
  /*
   *	tp,gp and ra are always preserved by the syscall
   *	s0-s11 are preserved except by fork
   */
  fprintf(fp, "\tli t1, %d\n", n);
  fprintf(fp, "\tecall\n");
  fprintf(fp, "\tla t1,errno\n");
  fprintf(fp, "\tsw a0,0(t1)\n");
  if (saves) {
    fprintf(fp, "\tlw s0,0(sp)\n");
    fprintf(fp, "\tlw s1,4(sp)\n");
    fprintf(fp, "\tlw s2,8(sp)\n");
    fprintf(fp, "\tlw s3,12(sp)\n");
    fprintf(fp, "\tlw s4,16(sp)\n");
    fprintf(fp, "\tlw s5,20(sp)\n");
    fprintf(fp, "\tlw s6,24(sp)\n");
    fprintf(fp, "\tlw s7,28(sp)\n");
    fprintf(fp, "\tlw s8,32(sp)\n");
    fprintf(fp, "\tlw s9,36(sp)\n");
    fprintf(fp, "\tlw s10,40(sp)\n");
    fprintf(fp, "\tlw s11,44(sp)\n");
    fprintf(fp, "\taddi sp,sp,48\n");
  }
  /* a0 holds the return from the call already */
  fprintf(fp, "\tjr 0(ra)\n");
  fclose(fp);
}

static void write_call_table(void)
{
  int i;
  for (i = 0; i < NR_SYSCALL; i++)
    write_call(i);
}

static void write_makefile(void)
{
  int i;
  FILE *fp = fopen("fuzixriscv32/Makefile", "w");
  if (fp == NULL) {
    perror("Makefile");
    exit(1);
  }
  fprintf(fp, "# Autogenerated by tools/syscall_riscv32\n");
  fprintf(fp, "CROSS_AS=riscv64-linux-gnu-gcc\nCROSS_LD=riscv64-linux-gnu-ld\nCROSS_AR=riscv64-linux-gnu-ar\n");
  fprintf(fp, "ASOPTS=-march=rv32ima -mabi=ilp32\n\n");
  fprintf(fp, "ASRCS = syscall_%s.S\n", syscall_name[0]);
  for (i = 1; i < NR_SYSCALL; i++)
    fprintf(fp, "ASRCS += syscall_%s.S\n", syscall_name[i]);
  fprintf(fp, "\n\nASRCALL = $(ASRCS) $(ASYS)\n");
  fprintf(fp, "\nAOBJS = $(ASRCALL:.S=.o)\n\n");
  fprintf(fp, "syslib.lib: $(AOBJS)\n");
  fprintf(fp, "\techo $(AOBJS) >syslib.l\n");
  fprintf(fp, "\t$(CROSS_AR) rc syslib.lib $(AOBJS)\n\n");
  fprintf(fp, "$(AOBJS): %%.o: %%.S\n");
  fprintf(fp, "\t$(CROSS_AS) $(ASOPTS) -c $<\n\n");
  fprintf(fp, "clean:\n");
  fprintf(fp, "\trm -f $(AOBJS) $(ASRCS) syslib.lib syslib.l *~\n\n");
  fclose(fp);
}

int main(int argc, char *argv[])
{
  write_makefile();
  write_call_table();
  exit(0);
}
