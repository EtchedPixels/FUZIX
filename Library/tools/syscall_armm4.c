/*
 *	Generate the syscall functions
 */

#include "syscall_name.h"
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

static char namebuf[128];

static void write_call(int n) {
  FILE *fp;
  int saves = 0;
  snprintf(namebuf, 128, "fuzixarmm4/syscall_%s.S", syscall_name[n]);
  fp = fopen(namebuf, "w");
  if (fp == NULL) {
    perror(namebuf);
    exit(1);
  }

  fprintf(fp,
		  "\t.code 16\n"
          "\t.text\n"
		  "\t.thumb_func\n"
          "\t.align 2\n"
          "\t.globl %1$s\n\n"
          "%1$s:\n",
          syscall_name[n]);

  fprintf(fp, "\tsvc #%d\n", n);
  fprintf(fp, "\tb _syscall_ret\n");

  fclose(fp);
}

static void write_call_table(void) {
  int i;
  for (i = 0; i < NR_SYSCALL; i++)
    write_call(i);
}

static void write_makefile(void) {
  int i;
  FILE *fp = fopen("fuzixarmm4/Makefile", "w");
  if (fp == NULL) {
    perror("writing Makefile");
    exit(1);
  }
  fprintf(fp, "# Autogenerated by tools/syscall_armm4\n");
  fprintf(fp, "CROSS_PATH=$(HOME)/tm4c129x/toolchain\n");
  fprintf(fp, "CROSS_PATH_BIN=$(CROSS_PATH)/bin\n");
  fprintf(fp, "CROSS_CC=$(CROSS_PATH_BIN)/arm-none-eabi-gcc\n");
  fprintf(fp, "CROSS_AS=$(CROSS_PATH_BIN)/arm-none-eabi-as\n");
  fprintf(fp, "CROSS_AR=$(CROSS_PATH_BIN)/arm-none-eabi-ar\n");
  fprintf(fp, "ASOPTS=-g -mcpu=cortex-m4 -march=armv7e-m+nofp -mthumb\n\n");
  fprintf(fp, "ASYS = ../fuzixarmm0/syscall_ret.S\n");
  fprintf(fp, "ASRCS =\n");
  for (i = 0; i < NR_SYSCALL; i++)
    fprintf(fp, "ASRCS += syscall_%s.S\n", syscall_name[i]);
  fprintf(fp, "\n\nASRCALL = $(ASRCS) $(ASYS)\n");
  fprintf(fp, "\nAOBJS = $(ASRCALL:.S=.o)\n\n");
  fprintf(fp, "syslib.lib: $(AOBJS)\n");
  fprintf(fp, "\techo $(AOBJS) >syslib.l\n");
  fprintf(fp, "\t$(CROSS_AR) rc syslib.lib $(AOBJS)\n\n");
  fprintf(fp, "$(AOBJS): %%.o: %%.S\n");
  fprintf(fp, "\t$(CROSS_AS) $(ASOPTS) -c -o $@ $<\n\n");
  fprintf(fp, "clean:\n");
  fprintf(fp, "\trm -f $(AOBJS) $(ASRCS) syslib.lib syslib.l *~\n\n");
  fclose(fp);
}

int main(int argc, char *argv[]) {
  write_makefile();
  write_call_table();
  exit(0);
}
